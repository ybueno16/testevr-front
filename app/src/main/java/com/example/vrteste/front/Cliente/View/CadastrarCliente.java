    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nome = jTextPane1.getText();
            String razaoSocial = jTextPane2.getText();
            String nomeFantasia = jTextPane3.getText();
            String cnpj = jTextPane4.getText();
            boolean ativo = jCheckBox1.isSelected();

            ClienteModel cliente = new ClienteModel(null, nome, razaoSocial, nomeFantasia, cnpj, ativo);
            ClienteApiController.cadastrarCliente(cliente);
            javax.swing.JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
            if (clienteView != null) {
                clienteView.carregarClientesNaTabela();
                clienteView.setVisible(true);
                clienteView.toFront();
            }
            this.dispose();
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar cliente: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

